export const getGraphClient = (chainId: number, queryBlock: boolean = false) => {
    switch (+chainId) {
        case ChainId.MAINNET:
            return queryBlock ? blockClient : client
        case ChainId.ARBITRUM_ONE:
            return queryBlock ? arbitrumBlockClient : arbitrumClient
        case ChainId.OPTIMISM:
            return queryBlock ? optimismBlockClient: optimismClient
        case ChainId.BNB:
            return queryBlock ? bscBlockClient : bscClient
        case ChainId.BASE:
            return queryBlock ? baseBlockClient : baseClient
        case ChainId.CELO:
            return queryBlock ? celoBlockClient : celoClient
        case ChainId.POLYGON:
            return queryBlock ? polygonBlockClient: polygonClient
        case ChainId.AVALANCHE:
            return queryBlock ? avalancheBlockClient : avalancheClient
        default:
            throw new Error('unsupport chainId: ' + chainId)
    }
}
